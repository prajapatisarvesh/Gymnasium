class RaceTracksEnv(gym.Env):
    """
    """
    metadata = {
        "render_modes": ["human", "rgb_array"],
        "render_fps": 4,
    }

    def __init__(self, track: Optional[str]="track0", render_mode: Optional[str] = None):
        if track == "track0":
            track = track0()
        elif track == "track1":
            track = track1()
        
        self.rows = track.shape[0]
        self.cols = track.shape[1]
        self.render_mode = render_mode
        self.start_states = []
        self.goal_states = []
        self.walls = []
        self.vel_limit = 5
        for i in range(0,self.rows):
            for j in range(0, self.cols):
                if track[i][j] == 2:
                    self.start_states.append((i,j))
                elif track[i][j] == 3:
                    self.goal_states.append((i,j))
                elif track[i][j] == 1:
                    self.walls.append((i,j))
        
        self.goal_pos = self.goal_states
        self.agent_pos = None
        self.agent_vel = None
        self.action_space = spaces.Discrete(len(Action))
        self.observation_space = spaces.Tuple(
            (spaces.Discrete(self.rows), spaces.Discrete(self.cols))
        )


    def _get_obs(self):
        return self.agent_pos
    
    def reset(self, seed=None) -> Tuple[int, int]:
        """
        """
        self.agent_pos = self.start_states[np.random.randint(len(self.start_states))]
        self.agent_vel = (0, 0)
        return self.agent_pos
    
    def step(self, action: Action) -> Tuple[Tuple[int, int], float, bool, dict]:
        projected_state = np.array(self.agent_vel)
        projected_state[0] = -projected_state[0]
        projected_state += np.array(self.agent_pos)
        projected_state = tuple(projected_state)
        done = False
        cols_ = [y for x,y in self.goal_pos]
        if projected_state in self.goal_pos or ((projected_state[0] > self.rows) and projected_state[1] in cols_):
            reward = 1
            done = True
            projected_state = list(projected_state)
            projected_state[0] = self.goal_pos[0][0]
            projected_state = tuple(projected_state)
            self.agent_pos = projected_state
        elif  projected_state in self.walls or (projected_state[0] < 0 or projected_state[0] > self.rows) or \
            (projected_state[1] < 0 or projected_state[1] > self.cols):
            self.agent_pos = self.start_states[np.random.randint(len(self.start_states))]
            self.agent_vel = (0, 0)
            reward = -1
        
        else:
            self.agent_pos = projected_state
            reward = -1
        next_vel = tuple(map(sum, zip(self.agent_vel, actions_to_dxdy(action))))
        next_vel = tuple(np.maximum(0, np.array(next_vel)) % (self.vel_limit+1))
        self.agent_vel = next_vel
        return self._get_obs(), reward, done, {}
        
            
