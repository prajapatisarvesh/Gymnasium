class Action(IntEnum):
    '''Action'''

    INCREASE_X_INCREASE_Y = 0
    INCREASE_X_DECREASE_Y = 1
    INCREASE_X_STOP_Y = 2
    DECREASE_X_INCREASE_Y = 3
