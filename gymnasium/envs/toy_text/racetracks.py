def actions_to_dxdy(action: Action) -> Tuple[int, int]:
    """
    Helper function to map action to changes in x and y coordinates
    Args:
        action (Action): taken action
    Returns:
        dxdy (Tuple[int, int]): Change in x and y coordinates
    """
    mapping = {
        Action.INCREASE_X_INCREASE_Y: (1,1),
        Action.INCREASE_X_DECREASE_Y: (-1,1),
        Action.INCREASE_X_STOP_Y: (0,1),
        Action.DECREASE_X_INCREASE_Y: (1,-1),
        Action.DECREASE_X_DECREASE_Y: (-1,-1),
        Action.DECREASE_X_STOP_Y: (0,-1),
        Action.STOP_X_INCREASE_Y: (1,0),
        Action.STOP_X_DECREASE_Y: (-1, 0),
        Action.STOP_X_STOP_Y: (0,0),
    }
    return mapping[action]